export const getYearsList = () => {
  const currentYear = new Date().getFullYear() + 1; // Start from the next year
  const yearsList = [];

  for (let i = 0; i < 51; i++) {
    // Generate a list of 50 years before the next year
    const year = currentYear - i;
    yearsList.push({
      key: `${year}`,
      value: `${year}`,
    });
  }

  return yearsList;
};

